// Javascript Notes - Data Types, Variables & Assignment

// Number
let integerNumber = 3; 
let floatNumber = 3.5;
console.log(`Type of integerNumber - ${typeof integerNumber}`);
console.log(`Type of floatNumber - ${typeof floatNumber}`);

// Big Int
let bigIntNumber = 3n;
console.log(`Type of bigIntNumber - ${typeof bigIntNumber}`);

// Boolean
let booleanValue = false;
console.log(`Type of booleanValue - ${typeof booleanValue}`);

// String
let myString = 'ExampleString'
console.log(`Type of myString - ${typeof myString}`);

// Null
let nullExample = null;
console.log(`Type of nullExample - ${typeof nullExample}`);

// Undefined
let undefinedExample = undefined;
console.log(`Type of undefinedExample - ${typeof undefinedExample}`);

// Object
let objectExample = {};
console.log(`Type of objectExample - ${typeof objectExample}`);


